// add-estudantes
$('.add-estudantes').mouseenter(function() {
    $('.l-add-estudantes').css({
      'visibility': 'visible',
      'animation': 'icon_legend-show ease-in .5s'
    });
  })
  .mouseleave(function() {
    $('.l-add-estudantes').css({
      'visibility': 'hidden',
      'animation': 'icon_legend-hide ease-in .6s'
    });
});
